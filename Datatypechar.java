public class Datatypechar
{
    public static void main ( String[] args )
    {

        char A = 30;
        char B = 5;

        System.out.println ( A + B );
        System.out.println ( A - B );
        System.out.println ( A * B );
        System.out.println ( A / B );
        System.out.println ( A++ );
        System.out.println ( B-- );
        System.out.println ( ++A + --B );
        System.out.println ( ++A - --B );
    }

}
